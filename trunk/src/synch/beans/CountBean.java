package synch.beans;

public class CountBean {
	
	private boolean state;
	
	private String corCount="0";
	
	private String pxtjCount="0";
	
	private String sccpCount="0";
	
	private String scclCount="0";
	
	private String sczjCount="0";
	
	private String siteCount="0";
	
	private String whCount="0";

	public String getCorCount() {
		return corCount;
	}

	public void setCorCount(String corCount) {
		this.corCount = corCount;
	}

	public String getPxtjCount() {
		return pxtjCount;
	}

	public void setPxtjCount(String pxtjCount) {
		this.pxtjCount = pxtjCount;
	}

	public String getScclCount() {
		return scclCount;
	}

	public void setScclCount(String scclCount) {
		this.scclCount = scclCount;
	}

	public String getSccpCount() {
		return sccpCount;
	}

	public void setSccpCount(String sccpCount) {
		this.sccpCount = sccpCount;
	}

	public String getSczjCount() {
		return sczjCount;
	}

	public void setSczjCount(String sczjCount) {
		this.sczjCount = sczjCount;
	}

	public String getSiteCount() {
		return siteCount;
	}

	public void setSiteCount(String siteCount) {
		this.siteCount = siteCount;
	}

	public boolean isState() {
		return state;
	}

	public void setState(boolean state) {
		this.state = state;
	}

	public String getWhCount() {
		return whCount;
	}

	public void setWhCount(String whCount) {
		this.whCount = whCount;
	}
	
	

}
